"""
This module checks if the sum of two numbers equals a specific value.
"""

def main():
    """
    Main function to calculate and verify the sum of two numbers.
    """
    a = 12  # a is variable 1
    b = 1   # b is variable 2
    c = a + b  # c is variable which holds the answer

    if c == 13:  # case if answer is right
        print("Answer is 13")  # if it is right
    else:  # or
        print("wrong")  # wrong

if __name__ == "__main__":
    main()
